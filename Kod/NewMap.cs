public class Map
{
    private string[] mapData;
    public Map()
    {
        mapData = new string[] {

        "###########                ##############",
        "#.........#                #..$.........#          ###############",
        "#.........##################............############..........%..#",
        "#.......................................*........................#",
        "#.........#########.########............#########.################",
        "#.........#       #.#      #.........(..#       #.#       ############",
        "###########       #.#      ##############       #.#       #.......(..#",
        "                  #.#                           #.#       #..........#",
        "                  #.#                           #.#       #..........#",
        "                  #.#                           #.#       #..........#",
        "                  #.#                           #.#########.........$#",
        "       ############.#####                       #....................#",
        "       #..%.............#        ###########################.........################################",
        "       #................#        #..........&.#            #.........*..............................*",
        "       #............$...#        #............##############.........################################",
        "       ##################        #...$.....................^.........#",
        "                                 #####################################"

        };
    }
    
    public char GetCellAt(Point point)
    {
        string previousRow = mapData[point.Y];
        char previousCell = previousRow[point.X];

        return previousCell;
    }

    internal bool IsPointCorrect(Point point)
    {
        if (point.Y >= 0 && point.Y < mapData.Length)
        {
            if (point.X >= 0 && point.X < mapData[point.Y].Length)
            {
                if (GetCellAt(point) != '#')
                {
                    return true;
                }
            }
        }

        return false;
    }

    public void DisplayMap()
    {
        foreach (string row in mapData)
        {
            Console.WriteLine(row);
        }
    }
}